@chex.dataclass
class TwoAgentState:
    pos_0:     jnp.ndarray
    pos_1:     jnp.ndarray
    coin_r:    jnp.ndarray
    coin_b:    jnp.ndarray
    step_count:int

# Five actions: right, left, down, up, stay
MOVES = jnp.array([
    [0,  1],   # right
    [0, -1],   # left
    [-1,  0],   # down
    [1, 0],   # up
    [0,  0],   # stay
])

class Policy(eqx.Module):
    """Two-layer MLP → action logits."""
    linear1: eqx.nn.Linear
    linear2: eqx.nn.Linear
    linear3: eqx.nn.Linear

    def __init__(self, obs_dim: int, n_actions: int, key):
        k1, k2, k3 = jax.random.split(key, 3)
        self.linear1 = eqx.nn.Linear(obs_dim,   64, key=k1)
        self.linear2 = eqx.nn.Linear(64,        64, key=k2)
        self.linear3 = eqx.nn.Linear(64, n_actions, key=k3)

    def __call__(self, obs):
        x = obs.astype(jnp.float32)
        x = jax.nn.tanh(self.linear1(x))
        x = jax.nn.tanh(self.linear2(x))
        x = self.linear3(x)
        return x

@eqx.filter_jit
def sample_action(policy, obs, key):
    """Sample an action and return its log-probability and probability."""
    logits   = policy(obs)
    action   = jax.random.categorical(key, logits)
    log_probs = jax.nn.log_softmax(logits)
    log_prob = log_probs[action]
    probs = jnp.exp(log_probs)
    prob = probs[action]
    return action, log_prob, prob

class TwoPlayerCoinGame:
    def __init__(self, grid_size: int = 3, episode_length: int = 100):
        self.grid_size = grid_size
        self.episode_length = episode_length
        self.agents = ["agent_0", "agent_1"]

    @functools.partial(jit, static_argnums=(0,))
    def _state_to_obs(self, state: TwoAgentState):
        g = self.grid_size

        # Red perspective: [self=red, enemy=blue, own_coin=red, enemy_coin=blue]
        grid_r = jnp.zeros((g, g, 4), dtype=jnp.float32)
        grid_r = grid_r.at[state.pos_0[0], state.pos_0[1], 0].set(1.0)
        grid_r = grid_r.at[state.pos_1[0], state.pos_1[1], 1].set(1.0)
        grid_r = grid_r.at[state.coin_r[0], state.coin_r[1], 2].set(1.0)
        grid_r = grid_r.at[state.coin_b[0], state.coin_b[1], 3].set(1.0)

        # Blue perspective: [self=blue, enemy=red, own_coin=blue, enemy_coin=red]
        grid_b = jnp.zeros((g, g, 4), dtype=jnp.float32)
        grid_b = grid_b.at[state.pos_1[0], state.pos_1[1], 0].set(1.0)
        grid_b = grid_b.at[state.pos_0[0], state.pos_0[1], 1].set(1.0)
        grid_b = grid_b.at[state.coin_b[0], state.coin_b[1], 2].set(1.0)
        grid_b = grid_b.at[state.coin_r[0], state.coin_r[1], 3].set(1.0)

        return {"agent_0": grid_r.flatten(), "agent_1": grid_b.flatten()}

    @functools.partial(jit, static_argnums=(0,))
    def reset(self, key: jnp.ndarray):
        # 4 uniquely sampled starting positions to prevent overlapping
        flat_idx = jax.random.choice(key, self.grid_size**2, shape=(4,), replace=False)
        def to_2d(idx): return jnp.array([idx // self.grid_size, idx % self.grid_size])

        state = TwoAgentState(
            pos_0=to_2d(flat_idx[0]), pos_1=to_2d(flat_idx[1]),
            coin_r=to_2d(flat_idx[2]), coin_b=to_2d(flat_idx[3]),
            step_count=0
        )
        return self._state_to_obs(state), state

    @functools.partial(jit, static_argnums=(0,))
    def step(self, key: jnp.ndarray, state: TwoAgentState, actions: dict):
        new_pos_0 = (state.pos_0 + MOVES[actions["agent_0"]]) % self.grid_size
        new_pos_1 = (state.pos_1 + MOVES[actions["agent_1"]]) % self.grid_size

        r_gets_cr = jnp.all(new_pos_0 == state.coin_r)
        r_gets_cb = jnp.all(new_pos_0 == state.coin_b)
        b_gets_cb = jnp.all(new_pos_1 == state.coin_b)
        b_gets_cr = jnp.all(new_pos_1 == state.coin_r)

        # The Social Dilemma Rewards
        reward_r = (r_gets_cr * 1.0) - (r_gets_cb * 1.0)
        reward_b = (b_gets_cb * 1.0) - (b_gets_cr * 1.0)

        k1, k2 = jax.random.split(key)
        G = self.grid_size
        def respawn(keep_coin, rand_key):
            flat_keep = keep_coin[0] * G + keep_coin[1]
            valid = jnp.ones(G*G, dtype=jnp.float32).at[flat_keep].set(0.0)
            new_flat = jax.random.choice(rand_key, G*G, p=valid/valid.sum())
            return jnp.array([new_flat // G, new_flat % G])

        any_gets_cr = r_gets_cr | b_gets_cr
        any_gets_cb = r_gets_cb | b_gets_cb

        new_coin_r = jnp.where(any_gets_cr, respawn(state.coin_b, k1), state.coin_r)
        new_coin_b = jnp.where(any_gets_cb, respawn(new_coin_r, k2), state.coin_b)

        new_step = state.step_count + 1
        next_state = TwoAgentState(
            pos_0=new_pos_0, pos_1=new_pos_1,
            coin_r=new_coin_r, coin_b=new_coin_b,
            step_count=new_step
        )
        return self._state_to_obs(next_state), next_state, {"agent_0": reward_r, "agent_1": reward_b}, new_step >= self.episode_length

