"""# ── 3. Multi-Agent Co-Training ─────────────────

Agents learn **independently** — each only sees its own rewards.
"""

def discounted_returns(rewards: list, gamma: float) -> jnp.ndarray:
    """Compute G_t = r_t + γ r_{t+1} + γ² r_{t+2} + ... for each t."""
    R, returns = 0.0, []
    for r in reversed(rewards):
        R = float(r) + gamma * R
        returns.insert(0, R)
    G = jnp.array(returns)
    # Normalize for stable gradients
    return (G - G.mean()) / (G.std() + 1e-8)


@jit
def reinforce_loss(policy, obs_batch, actions_batch, returns):
    # 1. Run the forward pass INSIDE the loss function
    logits = jax.vmap(policy)(obs_batch)

    # 2. Compute log probabilities for all actions
    log_probs_all = jax.nn.log_softmax(logits)

    # 3. Select the log probabilities of the actions actually taken
    log_probs = log_probs_all[jnp.arange(len(actions_batch)), actions_batch]

    # 4. Calculate the REINFORCE objective
    return -jnp.mean(log_probs * returns)

@eqx.filter_jit
def update_agent(policy, opt_state, optimizer, obs_batch, actions_batch, returns):
    # 1. Calculate gradients
    loss, grads = jax.value_and_grad(reinforce_loss)(policy, obs_batch, actions_batch, returns)
    # 2. Update optimizer state
    updates, new_opt_state = optimizer.update(grads, opt_state)
    # 3. Apply updates to the policy
    new_policy = optax.apply_updates(policy, updates)

    return new_policy, new_opt_state, loss

def train_multiagent(pretrained_path, config):
    print(f"Loading pretrained weights from: {pretrained_path}")
    env = TwoPlayerCoinGame(config['GRID_SIZE'], config['EPISODE_LENGTH'])

    # 1. Load the pretrained single-agent model
    with open(os.path.join(pretrained_path, "models.pkl"), "rb") as f:
        pretrained_models = pickle.load(f)
    pretrained_policy = pretrained_models["agent_0"]

    # 2. Clone it so both agents start highly capable, but learn independently
    policies = {
        "agent_0": copy.deepcopy(pretrained_policy),
        "agent_1": copy.deepcopy(pretrained_policy)
    }

    # 3. Initialize independent optimizers
    optimizers, opt_states = {}, {}
    for agent in env.agents:
        opt = optax.adam(config['LR'])
        optimizers[agent] = opt
        opt_states[agent] = opt.init(eqx.filter(policies[agent], eqx.is_array))

    history = []
    key = jax.random.PRNGKey(42)

    # Use integer division (//) to avoid TypeError
    for ep in range(config['NUM_EPISODES']):
        key, ep_key = jax.random.split(key)
        obs, state = env.reset(ep_key)

        traj = {ag: {'obs': [], 'actions': [], 'rewards': []} for ag in env.agents}

        # Track coins per agent for this episode
        ep_coins = {
            'agent_0': {'red': 0, 'blue': 0},
            'agent_1': {'red': 0, 'blue': 0}
        }

        done = False

        # ── Rollout ──
        while not done:
            key, k1, k2 = jax.random.split(key, 3)

            # Fast JIT-compiled action sampling
            a0, _, _ = sample_action(policies["agent_0"], obs["agent_0"], k1)
            a1, _, _ = sample_action(policies["agent_1"], obs["agent_1"], k2)
            actions = {"agent_0": a0, "agent_1": a1}

            for ag in env.agents:
                traj[ag]['obs'].append(obs[ag])
                traj[ag]['actions'].append(actions[ag])

            # Snapshot old coin positions before the step
            old_cr, old_cb = state.coin_r, state.coin_b

            key, step_key = jax.random.split(key)
            obs, next_state, rewards, done = env.step(step_key, state, actions)

            # Check who picked up what by comparing new positions to old coin locations
            a0_pos, a1_pos = next_state.pos_0, next_state.pos_1

            ep_coins['agent_0']['red']  += int(jnp.all(a0_pos == old_cr))
            ep_coins['agent_0']['blue'] += int(jnp.all(a0_pos == old_cb))

            ep_coins['agent_1']['red']  += int(jnp.all(a1_pos == old_cr))
            ep_coins['agent_1']['blue'] += int(jnp.all(a1_pos == old_cb))

            state = next_state

            for ag in env.agents:
                traj[ag]['rewards'].append(float(rewards[ag]))

        # ── Update ──
        for agent in env.agents:
            obs_b = jnp.stack(traj[agent]['obs'])
            actions_b = jnp.array(traj[agent]['actions'])
            returns = discounted_returns(traj[agent]['rewards'], config['GAMMA'])

            policies[agent], opt_states[agent], _ = update_agent(
                policies[agent], opt_states[agent], optimizers[agent], obs_b, actions_b, returns
            )

        ep_r0 = sum(traj['agent_0']['rewards'])
        ep_r1 = sum(traj['agent_1']['rewards'])

        # Save rewards and all 4 coin counters
        history.append((
            ep, ep_r0, ep_r1,
            ep_coins['agent_0']['red'], ep_coins['agent_0']['blue'],
            ep_coins['agent_1']['red'], ep_coins['agent_1']['blue']
        ))

        if ep % config['SHOW_EVERY'] == 0:
            print(f"MA Ep {ep:4d}  |  r_0={ep_r0:+.1f} (R:{ep_coins['agent_0']['red']} B:{ep_coins['agent_0']['blue']})  |  r_1={ep_r1:+.1f} (R:{ep_coins['agent_1']['red']} B:{ep_coins['agent_1']['blue']})")

    # ── Save Results ──
    timestamp = datetime.now().strftime("%Y-%m-%d_%H-%M-%S")
    save_path = os.path.join(config.get("SAVE_DIR", "./runs"), f"multiagent_{timestamp}")
    os.makedirs(save_path, exist_ok=True)

    csv_path = os.path.join(save_path, "stats.csv")
    with open(csv_path, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["episode", "reward_agent_0", "reward_agent_1", "a0_red_coins", "a0_blue_coins", "a1_red_coins", "a1_blue_coins"])
        writer.writerows(history)

    with open(os.path.join(save_path, "models.pkl"), "wb") as f:
        pickle.dump(policies, f)

    print(f"\nDone! Co-trained models saved to: {save_path}")
    return policies, history, save_path

