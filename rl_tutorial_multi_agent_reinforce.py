def run_two_player_episode(save_path, config, seed=42):
    with open(os.path.join(save_path, "models.pkl"), "rb") as f:
        models = pickle.load(f)

    policy_0 = models["agent_0"]
    policy_1 = models.get("agent_1", models["agent_0"])

    env = TwoPlayerCoinGame(config['GRID_SIZE'], config['EPISODE_LENGTH'])
    key = jax.random.PRNGKey(seed)
    obs, state = env.reset(key)

    frames  = [state]
    rewards = {'agent_0': [], 'agent_1': []}
    coins   = {'agent_0': {'red': [], 'blue': []}, 'agent_1': {'red': [], 'blue': []}}
    done = False

    while not done:
        old_rc, old_bc = state.coin_r, state.coin_b

        actions = {
            "agent_0": int(jnp.argmax(policy_0(obs["agent_0"]))),
            "agent_1": int(jnp.argmax(policy_1(obs["agent_1"])))
        }
        key, sk = jax.random.split(key)

        obs, state, rws, done = env.step(sk, state, actions)

        frames.append(state)
        rewards['agent_0'].append(float(rws['agent_0']))
        rewards['agent_1'].append(float(rws['agent_1']))

        # Track which coin each agent picked
        coins['agent_0']['red'].append(int(jnp.all(state.pos_0 == old_rc)))
        coins['agent_0']['blue'].append(int(jnp.all(state.pos_0 == old_bc)))
        coins['agent_1']['red'].append(int(jnp.all(state.pos_1 == old_rc)))
        coins['agent_1']['blue'].append(int(jnp.all(state.pos_1 == old_bc)))

    return frames, rewards, coins

